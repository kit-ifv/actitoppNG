class FirstTourStart(private val rng: RNGHelper): DetermineFirstTourStart {
    override fun determineTourStart(input: MobilityPlanInputs, standardPreferredTourStart: ArrayHistogram?): Duration {
        val bounds = input.dayPlan.absoluteBoundsFor(input.tourPlan)
        return standardPreferredTourStart?.let {
            if(it.intersects(bounds)) {
                it.select(rng.randomValue, bounds.start.inWholeMinutes.toInt(), bounds.endInclusive.inWholeMinutes.toInt())
            } else {null}

        } ?: FIRST_TOUR_HISTOGRAM.select(rng, bounds, ) {
            MainDurationSituation(it, input)
        }
    }
}